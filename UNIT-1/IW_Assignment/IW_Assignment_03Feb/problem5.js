//  Given the days of the week in short format “Sun”, “Mon” ... print in long format “Sunday”, “Monday”, ...

var day = "Sat";

switch(day){

    case "sun":
        console.log("Sunday");
        break;

    case "Mon":
        console.log("Monday");
        break;
        
    case "Tue":
        console.log("Tuesday") ;

    case "Wen":
        console.log("Wednesday") ;

    case "Thru":
        console.log("Thursday") ;

    case "Fri":
        console.log("Friday") ;

    case "Sat":
        console.log("Saturday") ;
}